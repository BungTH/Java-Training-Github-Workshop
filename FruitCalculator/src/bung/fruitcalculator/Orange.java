package bung.fruitcalculator;

public class Orange {

	private String taste = "sour";
	private double weight;
	
	public String getTaste() {
		return taste;
	}
	
	public void setTaste(String tasteIn) {
		taste = tasteIn;
	}
	
	public double getWeight() {
		return weight;
	}
	
	public void setWeight(double weightIn) {
		weight = weightIn;
	}
	
}